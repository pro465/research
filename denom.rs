fn print_sol(a: &[usize], lim: usize) {
    let n = a.iter().max().copied().unwrap_or(0);
    let mut v = vec![false; n + 1];
    for &i in a {
        v[i] = true;
    }
    for i in 0..=n {
        for j in 0..=n {
            if i + j > lim {
                break;
            }
            if v[i] && v[j] {
                print!("#");
            } else {
                print!(" ");
            }
        }
        println!();
    }
}

